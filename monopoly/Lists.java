package ass3;

public class Lists {
	private String item;
	private int id;
	
	
	public Lists(String item,int id) {
		setItem(item);
		setId(id);
	}

	
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}

	
	public String getItem() {
		return item;
	}
	public void setItem(String item) {
		this.item = item;
	}
	
	
	
	
	
	
	
}
